# math
print("addition", 5 + 5)
print("subtraction", 10 - 3)
print("multiplication", 10 * 3)
print("division", 10 / 3)  # division
print("floor division", 10 // 3)
print("Modulus (remainder)", 10 % 4)  # modulus - gives the remainder
print("exponent", 10**2)

print(10 > 2)
print(10 == 2)
print(10 < 2)
print(10 != 2)
